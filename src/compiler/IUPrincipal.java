    private void jMenu1MenuSelected(javax.swing.event.MenuEvent evt) {//GEN-FIRST:event_jMenu1MenuSelected
        JFileChooser file = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Text txt","txt");
        file.setFileFilter(filter);
        int i = file.showSaveDialog(null);
        if(i==1){
            JOptionPane.showMessageDialog(null,"Erro na abertura de arquivo");
        } else {
            File arquivo = file.getSelectedFile();
            Scanner scanner = null;
            try {
                scanner = new Scanner(new FileReader(arquivo));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(IUPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            while(scanner.hasNext()){
                Language += scanner.nextLine()+"\n";
            }
            TextLanguage.setText(Language);
        }
    }//GEN-LAST:event_jMenu1MenuSelected

