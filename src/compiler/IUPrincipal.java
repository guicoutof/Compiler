    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<Token> tokens = new ArrayList();
        DefaultTableModel model = new DefaultTableModel();
        
        //      LEXICO
        Lexico lexico = new Lexico(TextLanguage.getText()+" ");
        tokens = lexico.analisar();
        
        //      IMPRESSAO TABELA
        model.setRowCount(0);
        model = (DefaultTableModel) TableLexico.getModel();
        
        for(int i=0;i<tokens.size();i++){
            Token atual = tokens.get(i);
            model.addRow(new String[]{atual.getLexema(),atual.getToken(),String.valueOf(atual.getLinha()),String.valueOf(atual.getColuna())});
            
        }
        
        //      SINTATICO
        ArrayList<String> msg;
        Sintatico sintatico = new Sintatico(tokens);
        sintatico.analisar();
        msg = sintatico.getMsg();
        
        model = (DefaultTableModel) TabelaSintatico.getModel();
        model.setRowCount(0);
        
        for(int i=0;i<msg.size();i++){
            model.addRow(new String[]{msg.get(i)});
        }
    }//GEN-LAST:event_jButton1ActionPerformed

